/** Jeden vynucený půltah ballotu: z pole `from` na cílové pole `to`. */
export interface Ply {
  readonly from: Square;
  readonly to: Square;
}

/** Ballot = tři vynucené půltahy zahájení (černý, bílý, černý). */
export type Ballot = readonly [Ply, Ply, Ply];

/** Výsledek odehrání ballotu: výsledná pozice + tři reálné odehrané tahy. */
export interface BallotResult {
  readonly position: Position;
  readonly moves: readonly Move[];
}

